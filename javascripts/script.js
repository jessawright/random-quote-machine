var a;
var sceneNum;

// Function to display text or media
function display() {
    "use strict";
    if (sceneNum === 1 || sceneNum === 3 || sceneNum === 5 || sceneNum === 7) {
        $("#quotemedia").removeAttr("src");
        $("#quotetext").html(quotes[sceneNum][a[sceneNum]].quote);
        $("#sourcetext").html(quotes[sceneNum][a[sceneNum]].source);
        $("#tweet").attr({
            href: "https://twitter.com/intent/tweet?text=" + encodeURI("\"" + quotes[sceneNum][a[sceneNum]].quote + "\" from " + quotes[sceneNum][a[sceneNum]].source),
            target: "_blank"
        });
    } else {
        $("#quotemedia").attr({
            src: quotes[sceneNum][a[sceneNum]].media
        });
        $("#quotetext").empty();
        $("#sourcetext").html(quotes[sceneNum][a[sceneNum]].source + "<a href=\"" + quotes[sceneNum][a[sceneNum]].link + "\" target=\"_blank\">  (via)</a>");
        $("#tweet").attr({
            href: "https://twitter.com/intent/tweet?text=" + encodeURI("A scene from " + quotes[sceneNum][a[sceneNum]].source + ": " + quotes[sceneNum][a[sceneNum]].link),
            target: "_blank"
        });
    }
}

function newMovie() {
    "use strict";
    a = [];
    sceneNum = 0;
    var i;
    for (i = 0; i < quotes.length; i++) {
        a.push(Math.floor(Math.random() * quotes[i].length));
    }

    display();
    $("#nextscene").removeAttr("disabled");
    $("#tweet").removeAttr("disabled");
}
// New Movie button
$("#newmovie").on("click", function () {
    "use strict";
    newMovie();
});

// Next Scene button
$("#nextscene").on("click", function () {
    "use strict";
    if (sceneNum < quotes.length - 2) {
        sceneNum++;
        display();
    } else if (sceneNum === quotes.length - 2) {
        sceneNum++;
        display();
        $("#nextscene").attr("disabled", "disabled");
    }
    if (sceneNum > 0) {
        $("#lastscene").removeAttr("disabled");
    }
});

// Last Scene button
$("#lastscene").on("click", function () {
    "use strict";
    if (sceneNum > 0) {
        sceneNum--;
        display();
    }
    if (sceneNum === 0) {
        $("#lastscene").attr("disabled", "disabled");
    }
    if (sceneNum < quotes.length - 1) {
        $("#nextscene").removeAttr("disabled");
    }
});

// Start a new movie on loading
document.ready(newMovie());
